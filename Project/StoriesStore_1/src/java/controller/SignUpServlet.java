    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        String user = request.getParameter("username");
        String pass = request.getParameter("password");
        String re_pass = request.getParameter("repassword");
//        không nhập gì
        if (!pass.equals(re_pass)) {
//            signup không thành công
            request.setAttribute("errorSignUp2", "Password not same Repassword. Please re-enter Rassword & Repassword");
            request.getRequestDispatcher("SignUp.jsp").forward(request, response);
        } else {
            AdminDAO ad = new AdminDAO();
            Admin a = ad.getAdminByUser(user);
            if (a == null) {
//              user không tồn tại được signup
                Admin a1 = new Admin(user, pass);
                ad.insertAdmin(a1);
                request.setAttribute("errorSignUp3", "SignUp successful. Please Login");
                request.getRequestDispatcher("Login.jsp").forward(request, response);
            } else {
                request.setAttribute("errorSignUp4", "Username already exist. Please re-enter Username");
                request.getRequestDispatcher("SignUp.jsp").forward(request, response);
            }
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

